export class Point {
  constructor(x, y) {
    this.x = x
    this.y = y
  }
}

export class Size {
  constructor(width, height) {
    this.width = width
    this.height = height
  }
}

export class Rect{
  constructor(x, y, width, height) {
    this.x = x
    this.y = y
    this.width = width
    this.height = height
  }
}